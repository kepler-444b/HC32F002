/**
 ******************************************************************************
 ** \brief  Main function of project
 **
 ** \return uint32_t return value, if needed
 **
 ** This sample
 **
 ******************************************************************************/

int main(void)
{
    uint32_t  u32Addr        = 0x3F00;
    uint8_t   u8TestData[16] = {0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F};
    uint8_t  *pu8DataAddr    = (uint8_t*)u32Addr;
    
    ///< FLASH扇区擦除
    while(Ok != FLASH_SectorErase(u32Addr))
    {
        ;
    }
    
    ///< FLASH 字节写
    while(Ok != FLASH_WriteByte(u32Addr, u8TestData, 16))
    {
        ;
    }
    
    ///< FLASH 数据校验
    while(Ok != FLASH_DataVerify(pu8DataAddr, u8TestData, 16))
    {
        ;
    }
    
    while (1);
}

